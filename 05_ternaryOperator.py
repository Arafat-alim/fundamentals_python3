# Ternary Conditional Operator Example

meaning = 100  # Initial value assigned to the variable 'meaning'

# Ternary conditional statement to check if 'meaning' is greater than 50
print("Meaning is greater than 50") if meaning > 50 else print("Meaning is less than 50")
# This single line of code is equivalent to:
# if meaning > 50:
#     print("Meaning is greater than 50")
# else:
#     print("Meaning is less than 50")
